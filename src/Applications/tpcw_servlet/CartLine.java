package Applications.tpcw_servlet;

import java.io.Externalizable;
import java.io.IOException;
import java.io.ObjectInput;
import java.io.ObjectOutput;

public class CartLine implements Externalizable {
    private static final long serialVersionUID = 8562380891368675197L;

    public String scl_title;
    public double scl_cost;
    public double scl_srp;
    public String scl_backing;
    public int scl_qty;
    public int scl_i_id;

    @Override
    public void writeExternal(ObjectOutput out) throws IOException {
        out.writeLong(serialVersionUID);

        out.writeUTF(scl_title);
        out.writeDouble(scl_cost);
        out.writeDouble(scl_srp);
        out.writeUTF(scl_backing);
        out.writeInt(scl_qty);
        out.writeInt(scl_i_id);
    }

    @Override
    public void readExternal(ObjectInput in) throws IOException,
            ClassNotFoundException {
        long classId = in.readLong();
        assert (classId == serialVersionUID);

        this.scl_title = in.readUTF();
        this.scl_cost = in.readDouble();
        this.scl_srp = in.readDouble();
        this.scl_backing = in.readUTF();
        this.scl_qty = in.readInt();
        this.scl_i_id = in.readInt();
    }

    public CartLine() {
    }

    public CartLine(String title, double cost, double srp, String backing,
                    int qty, int id) {
        scl_title = title;
        scl_cost = cost;
        scl_srp = srp;
        scl_backing = backing;
        scl_qty = qty;
        scl_i_id = id;
    }
}
